#include <stddef.h>

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	int		cmp;

	i = -1;
	while (++i < n)
		if ((cmp = ((unsigned char *)s1)[i] - ((unsigned char *)s2)[i]))
			return (cmp);
	return (0);
}
